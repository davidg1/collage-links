function createList(list, arr, prefix) {

    for (i = 0; i < arr.length; i++) {
        let li = document.createElement('li');
                
        let a = document.createElement("a");
    
        a.setAttribute("target", "_blank");  
        a.setAttribute("href", arr[i]);  

        a.innerText = prefix + " Link " + i;
        
        li.appendChild(a);
        
        list.appendChild(li);
    }
    
}







